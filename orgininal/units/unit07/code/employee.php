<?php
  // Defining the Class
  class Employee { // Define the class
    public $name;  // The properties/variables
    public $address;
    public $phone;

    function printPersonInfo() // The methods
    {
      echo "<hr><b>Employee Info</b><br>";
      echo $this->name ."<br>\n";
      echo $this->address ."<br>\n";
      echo $this->phone ."<br>\n";
    }
  }
    
  // Create a new object & assign properties
  $Heidi = new Employee();
  $Heidi->name = "Heidi Clum";
  $Heidi->address = "1234 Somewhere Blvd ";
  $Heidi->phone = "123-456-7890";

  // Create another object
  $Brad = new Employee();     
  $Brad->name = "Brad Bit";
  $Brad->address = "4321 Sunset Blvd ";
  $Brad->phone = "987-654-3210";
  
  // Access the objects with the method
  $Heidi->printPersonInfo();
  $Brad->printPersonInfo();
?>
